export function findExerciseByName(name: string) {
  const n = name.trim().toLowerCase();
  const byName = exercises.find((e) => e.name.toLowerCase() === n);
  if (byName) return byName;
  const alias = nameAliases[n];
  return alias ? getExercise(alias) : undefined;
}

